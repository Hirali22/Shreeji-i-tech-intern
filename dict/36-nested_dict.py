# Nested dictionary for multiple students.
students = {
    "Hiral": {
        "rno": 1,
        "city": "jnd"
    },
    "Rahul": {
        "rno": 2,
        "city": "jnd"
    }
}
print(students)

# Accessing nested dictionary
print(students["Hiral"]["rno"])  # Output: 1
print(students["Rahul"]["city"])  # Output: jnd
